function switchChar(str) {
    
key=str[0];


switch (key) {
    case 'a':
    case 'e':
    case 'i':
    case 'o':
    case 'u':
    case 'y':
        return "A";
    case'j':
    case'k':
    case'l':
    case'n':

    return "B"


    default:
        break;
}

}

